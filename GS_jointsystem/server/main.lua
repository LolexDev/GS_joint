ESX.RegisterServerCallback('GS_jointsystem:provera', function(source, cb)
    local xPlayer = ESX.GetPlayerFromId(source)
    local trebaRizli = Config.TrebaRizli
    local trebaVutre = Config.TrebaVutre

    if xPlayer.getInventoryItem(Config.RizleItem).count >= trebaRizli and xPlayer.getInventoryItem(Config.VutraItem).count >= trebaVutre then
        cb(true)
    else
        cb(false)
    end
end)

ESX.RegisterServerCallback('GS_jointsystem:proveraJointa', function(source, cb)
    local xPlayer = ESX.GetPlayerFromId(source)

    if xPlayer.getInventoryItem('joint').count >= 1 then
        cb(true)
    else
        cb(false)
    end
end)

RegisterNetEvent('GS_jointsystem:rolledup', function()
    local player = ESX.GetPlayerFromId(source)
    local trebaRizli = Config.TrebaRizli
    local trebaVutre = Config.TrebaVutre

    player.removeInventoryItem(Config.RizleItem, trebaRizli)
    player.removeInventoryItem(Config.VutraItem, trebaVutre)
    player.addInventoryItem('joint', 1)
end)

RegisterNetEvent('GS_jointsystem:brvb', function ()
    local igrac = ESX.GetPlayerFromId(source)

    igrac.SetPedArmour(PlayerPedId(),100)
end)

ESX.RegisterUsableItem('papers', function(source)
    TriggerClientEvent('GS_jointsystem:rollup', source)
end)

ESX.RegisterUsableItem('joint', function(source)
    TriggerClientEvent('GS_jointsystem:smokingJoint', source)
end)