RegisterNetEvent("GS_jointsystem:rollup")
AddEventHandler("GS_jointsystem:rollup", function()
    ESX.TriggerServerCallback('GS_jointsystem:provera', function(ima)
        if ima then
            TaskStartScenarioInPlace(PlayerPedId(), "PROP_HUMAN_PARKING_METER", 0, true)
            progressbar(_U('rollingprog'), 5 * 1000)
            ClearPedTasks(PlayerPedId())
            TriggerServerEvent('GS_jointsystem:rolledup')
        end
    end)
end)

RegisterNetEvent("GS_jointsystem:smokingJoint")
AddEventHandler("GS_jointsystem:smokingJoint", function ()
    ESX.TriggerServerCallback("GS_jointsystem:proveraJointa", function (ima)
        if ima then
            TaskStartScenarioInPlace(PlayerPedId(), "WORLD_HUMAN_DRUG_DEALER", 0, true)
            progressbar(_U('smokingprogb'), 7 * 1000)
            ClearPedTasks(PlayerPedId())
            TriggerServerEvent('GS_jointsystem:brvb')
        end
    end)
end)